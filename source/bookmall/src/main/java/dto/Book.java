package dto;

public class Book {

	 private int book_id;
	 private String book_name;
	 private int book_price;
	 private int book_stock;
	 private String book_discount;
	 private String book_type;
	 private String book_content;
	 private String book_reg;
	 private int smallC;
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public int getBook_price() {
		return book_price;
	}
	public void setBook_price(int book_price) {
		this.book_price = book_price;
	}
	public int getBook_stock() {
		return book_stock;
	}
	public void setBook_stock(int book_stock) {
		this.book_stock = book_stock;
	}
	public String getBook_discount() {
		return book_discount;
	}
	public void setBook_discount(String book_discount) {
		this.book_discount = book_discount;
	}
	public String getBook_type() {
		return book_type;
	}
	public void setBook_type(String book_type) {
		this.book_type = book_type;
	}
	public String getBook_content() {
		return book_content;
	}
	public void setBook_content(String book_content) {
		this.book_content = book_content;
	}
	public String getBook_reg() {
		return book_reg;
	}
	public void setBook_reg(String book_reg) {
		this.book_reg = book_reg;
	}
	public int getSmallC() {
		return smallC;
	}
	public void setSmallC(int smallC) {
		this.smallC = smallC;
	}
	 
	 
}
